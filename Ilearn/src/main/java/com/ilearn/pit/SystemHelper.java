package com.ilearn.pit;

public class SystemHelper {

	public static String getProperty(String input) {
		return System.getProperty(input);
		
	}
	
	public static void arraycopy(char[] charArray, int i, char[] newCharArray, int j, int length) {
		System.arraycopy(charArray, i, newCharArray, j, length);
		
	}
	
	
	
}
